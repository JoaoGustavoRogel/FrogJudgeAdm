    private void consultaBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultaBtActionPerformed
        // TODO add your handling code here: consulta
        int idP = Integer.parseInt(idField.getText());
        ProblemaDAO prDAO = new ProblemaDAO();
        Problema pr = prDAO.buscaId(idP, Login.usuario.getId());
        
        if(pr != null) {    
            descField.setEditable(true);
            inField.setEditable(true);
            outField.setEditable(true);
            nomeField.setEditable(true);
            
            descField.setText(pr.getDescricao());
            inField.setText(pr.getInput());
            outField.setText(pr.getOutput());
            nomeField.setText(pr.getNome());
            editarBotao.setEnabled(true);
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "Problema não existe ou não é seu!");
        }
    }//GEN-LAST:event_consultaBtActionPerformed


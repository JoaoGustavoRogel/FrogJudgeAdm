    private void editarBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBotaoActionPerformed
        // Edita problema
        ProblemaDAO prDAO = new ProblemaDAO();
        Problema pr = new Problema(nomeField.getText(), inField.getText(), outField.getText(), descField.getText(), (true ? publico.isSelected() : false));

        int id = Integer.parseInt(idField.getText());
        prDAO.edita(pr, id);
        JOptionPane.showMessageDialog(rootPane, "Problema editado com sucesso!");
        
        limpaCampos();
    }//GEN-LAST:event_editarBotaoActionPerformed


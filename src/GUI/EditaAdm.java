    private void editarBotao3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBotao3ActionPerformed
        // Edita Usuário
        Administrador novo = new Administrador();
        novo.setNome(nameField.getText());
        novo.setSenha(senhaField.getText());

        AdministradorDAO admDAO = new AdministradorDAO();
        admDAO.edita(novo, Integer.parseInt(idField.getText()));
        JOptionPane.showMessageDialog(rootPane, "Adm editado com sucesso!");
        limpaCampos();
    }//GEN-LAST:event_editarBotao3ActionPerformed


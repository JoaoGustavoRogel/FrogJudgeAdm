    private void consultaBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultaBotaoActionPerformed
        // Busca consulta
        AdministradorDAO admDAO = new AdministradorDAO();
        Administrador res = admDAO.buscaId(Integer.parseInt(idField.getText()));
        if(res == null)
            JOptionPane.showMessageDialog(rootPane, "Usuário não encontrado!");
        else {
            nameField.setText(res.getNome());
            senhaField.setText("123");
            senhaField.setEditable(true);
            nameField.setEditable(true);
            editarBotao3.setEnabled(true);
        }
    }//GEN-LAST:event_consultaBotaoActionPerformed

